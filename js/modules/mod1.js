/**
 * Says hello.
 * @param {String} name a name
 * @return {String}     a greeting for `name`
 */
const sayHelloTo = name =>
  `Hello, ${name}!`;

/**
 * Says goodbye
 * @param {String} name a name
 * @return {String}     a farewell for `name`
 */
const sayGoodbyeTo = name =>
  `Later, ${name}!`;

export {
  sayHelloTo,
  sayGoodbyeTo,
};
